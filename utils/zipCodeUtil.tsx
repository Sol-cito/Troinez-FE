export default function getDeliveryPriceByZipCode(
  zipCode: string
): [number, string] {
  const DEFAULT_DELIVERY_PRICE = 3000;
  const ZEJU_DELIVERY_PRICE = 6000;
  const ISLAND_DELIVERY_PRICE = 8000;

  const numberZipCode = parseInt(zipCode, 10);
  const isZeju = zeju.find((elt) => elt === numberZipCode) !== undefined;
  if (isZeju) {
    return [ZEJU_DELIVERY_PRICE, '제주'];
  }

  const isIsland = isLand.find((elt) => elt === numberZipCode) !== undefined;
  if (isIsland) {
    return [ISLAND_DELIVERY_PRICE, '산간'];
  }

  return [DEFAULT_DELIVERY_PRICE, '일반'];
}
